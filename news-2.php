                            <p>
                                Komisioner Program Dan Data Komisi Pemilihan Umum (KPU) kota Batam, Martius mengatakan,
                                tim PPDP mulai bekerja pada 15 Juli 2020.
                            </p>

                            <p>
                                Sebanyak 2.222 TPS sebelumnya telah dipetakan KPU Batam.
                            </p>

                            <p>
                                "Ada beberapa TPS ditiadakan lantaran penggusuran, namun kami akan tetap akomodir sesuai
                                dengan PKPU nomor 5, Jika jumlah pemilih lebih dari 500 orang maka akan dibuka TPS
                                baru," ucapnya, Minggu (12/7/2020).
                            </p>

                            <p>
                                Sebelum turun untuk melaksanakan tugasnya, semua tim PPDP akan menjalani rapid tes
                                terlebih dahulu.
                            </p>

                            <h3>
                                Satu TPS Maksimal 500 Pemilih
                            </h3>
                            <p>

                                Untuk menyukseskan perhelatan pesta demokrasi 9 Desember mendatang, Pemko Batam
                                menyetujui penambahan anggaran pelaksanaan Pilkada.
                            </p>

                            <p>
                                Penambahan anggaran sebesar 1,5 milliar untuk pembelian logistik kebutuhan Pilkada.



                            </p>

                            <p>
                                Ketua Komisi Pemilihan Umum Kota Batam, Herrigen Agusti saat ditemui Kamis (2/7/2020)
                                membenarkan adanya suntikan dana yang diterima KPU Batam.
                            </p>

                            <p>
                                "Iya benar, kita sudah berkordinasi dengan Pemerintah Kota. Pengajuan anggaran yang kita
                                ajukan sudah di setujui oleh pemerintah kota dengan pembiayaan melalui ABPD Kota Batam,"
                                ujar Herrigen.
                            </p>

                            <p>
                                Kata dia, adanya suntikan dana ini digunakan untuk pembiayaan logistik Pilkada seperti
                                biaya penambahan TPS.
                            </p>

                            <p>
                                "Tahun ini, di tengah kondisi pendemi ada kebijakan di PKPU nomor 5 agar memerapkan
                                protokol kesehatan, penerapan itu disejalankan dengan penambahan TPS. Maka dengan
                                kalkulasi rincian, KPU Batam menambah jumlah TPS," ujarnya.
                            </p>

                            <p>
                                Penambahan TPS, kata dia, sudah diatur dalam PKPU.
                            </p>

                            <p>
                                "Jadi satu TPS sekarang dibatasi paling banyak 500 orang pemilih, jika lebih TPS akan
                                dipecah," katanya.
                            </p>
                            <p>
                                Bahkan saat ini, pemetaan TPS oleh KPU ada sebanyak 2.222 TPS yang akan disebar di
                                wilayah Batam pada pemilihan nantinya.
                            </p>

                            <h3>
                                KPU Karimun Rapid Test Penyelenggara Pemilu
                            </h3>

                            <p>
                                Dana hibah yang diterima KPU Karimun sebesar Rp 1,6 Miliar bakal digunakan untuk
                                pelaksanaan rapid test Covid-19.
                            </p>

                            <p>
                                Alokasi dana yang bersumber dari Anggaran Pendapatan Belanja Negara (APBN), nantinya
                                akan merapid test 1.041 penyelenggara Pemilu di Kabupaten Karimun.
                            </p>

                            <p>
                                Sebagai tahap awal, pelaksanan rapid test akan dilakukan kepada Petugas Pemutakhiran
                                Data Pemilih (PPDP) di Kabupaten Karimun.
                            </p>

                            <p>
                                Ini penting untuk memastikan mereka terbebas dari virus Corona.
                            </p>

                            <p>
                                Terdapat 557 petugas PPDP untuk memverifikasi data pemilih untuk menggunakan hak
                                pilihnya pada Pilbup Karimun 2020 ini.
                            </p>

                            <p>
                                Seperti diketahui, tahapan Pilkada Karimun telah memasuki masa pencocokan dan penelitian
                                (coklit) data pemilih.
                            </p>

                            <p>
                                Sesuai aturan, satu PPDP akan ditempatkan pada satu Tempat Pemungutan Suara (TPS).
                            </p>

                            <p>
                                "Test rapid ini sudah dianggarkan dalam anggaran pengadaan APD yang kita terima dari
                                Pemerintah Pusat. Seluruh petugas penyelenggara Pemilu akan menjalani rapid test ini.
                                Untuk tahap awal PPDP dulu. Intinya kami memastikan semua petugas penyelenggara Pemilu
                                bebas Covid-19," harapnya," ucap Ketua KPU Karimun, Eko Purwandoko, Kamis (9/7/2020).
                            </p>

                            <p>
                                Eko menyebutkan, apabila nantinya ada ditemukan petugas coklit reaktif virus Corona,
                                pihaknya akan langsung mengantinya dengan orang lain.

                            </p>

                            <p>
                                "Sebelum tanggal 15 ini, petugas-petugas itu harus sudah test rapid. Kami sudah
                                berkoordinasi bersama Tim Gugus Tugas," kata Ketua KPU Karimun, Eko Purwandoko, Kamis
                                (9/7/2020).
                            </p>

                            <p>
                                Untuk pelaksanaannya, lanjut Eko kemungkinan akan dilakukan di puskesmas-puskesmas yang
                                tersebar di Kabupaten Karimun.
                            </p>

                            <p>
                                Selain petugas Coklit kata Eko, Test Rapid juga dianggarkan kepada seluruh penyelenggara
                                pemilu dari tingkat PPS hingga kepada KPU Kabupaten.
                            </p>
