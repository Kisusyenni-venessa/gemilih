                                TRIBUNBATAM.id, BATAM - Menyambut Pilkada serentak 9 Desember mendatang, Komisi Pemilihan Umum (KPU) Kota Batam mulai merekrut 2.222 Petugas Pemutakhiran Data Pemilu (PPDP).
                            </p>

